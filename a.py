def tambah(angka1, angka2):
    hasil = angka1 + angka2
    return hasil

def kurang(angka1, angka2):
    hasil = angka1 - angka2
    return hasil

def kali(angka1, angka2):
    hasil = angka1 * angka2
    return hasil

def bagi(angka1, angka2):
    hasil = angka1 / angka2
    return hasil